package com.example.unittest.task1

class StringCalculator {
    fun plus(a: Int, b: Int): Int {
        return a + b
    }
    fun minus(a: Int, b: Int): Int {
        return a - b
    }
    fun divided(a: Int, b: Int): Int {
        return a / b
    }

    fun mutil(a: Int, b: Int): Int {
        return a * b
    }

}